struct GT_STATIC : GENBASE_DATA
{
	char fontName[GT_PATH];
	struct COLOR
	{
		unsigned char red, green, blue;
	} normalText, background;
    char shapeFile[GT_PATH];                        // vfx shape file

	enum drawtype
	{
		NODRAW,
		FILL,
		HASH
	} backgroundDraw;

	bool bBackdraw;
};
