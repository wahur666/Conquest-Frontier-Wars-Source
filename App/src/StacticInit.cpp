#include "pch.h"
#include "globals.h"

Vector circleLines[NUM_CIRCLE_SEGS];

class Initer
{
public:
	Initer()
	{
		for(int i = 0; i < NUM_CIRCLE_SEGS; ++i)
		{
			circleLines[i] = Vector(cos((2*PI*i)/NUM_CIRCLE_SEGS),
				sin((2*PI*i)/NUM_CIRCLE_SEGS),0);
		}
	}
};

static Initer initObj;

