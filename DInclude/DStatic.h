struct STATIC_DATA
{
    char staticType[GT_PATH];
	STTXT::STATIC_TEXT staticText;         // can also be used for ID
	STTXT::STATIC_TOOLTIP staticTooltip;
	STTXT::STATIC_HINTBOX staticHintbox;
	enum alignmenttype
	{
		LEFT,
		CENTER,
		RIGHT,
		TOPLEFT,
		TENBYSIX,
		LEFTSIX
	} alignment;
    S32 xOrigin, yOrigin;
	S32 width, height;				// if non-zero, override shape bounds
};

